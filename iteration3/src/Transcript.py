
    def gradeChangeToDouble(self, grade):
        if grade == "AA":
            return 4
        elif grade == "BA":
            return 3.5
        elif grade == "BB":
            return 3
        elif grade == "CB":
            return 2.5
        elif grade == "CC":
            return 2
        elif grade == "DC":
            return 1.5
        elif grade == "DD":
            return 1
        elif grade == "FD":
            return 0.5
        elif grade == "FF":
            return 0
